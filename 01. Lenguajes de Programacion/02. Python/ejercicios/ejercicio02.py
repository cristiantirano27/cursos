'''
Declarar multiples variables.
Pedir al usuario que ingrese el valor de 2 variables 
Realizar varias operaciones aritmeticas (Suma, resta, multiplicacion, division y division entera)
Mostrar el resultado con print.
'''
n1 = float(input("Ingrese el numero 1: "))
n2 = float(input("Ingrese el numero 2: "))
s = n1 + n2
r = n1 - n2
m = n1 * n2
d = n1 / n2
de = n1 // n2
print(f"Suma = {s}")
print(f"Resta = {r}")
print(f"Multiplicacion = {m}")
print(f"Division = {d}")
print(f"Division entera = {de}")